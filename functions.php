// Check if a property matches all selected features
function property_matches_features_global( $post_id, $selected_features ) {
    if ( empty( $selected_features ) ) return true;
    $feature_keywords = get_feature_keywords();
    $rows = get_field( 'key_features', $post_id );
    if ( empty( $rows ) ) return false;
    $feature_text = implode( '|', array_column( $rows, 'feature' ) );
    foreach ( $selected_features as $selected ) {
        if ( ! isset( $feature_keywords[ $selected ] ) ) continue;
        $matched = false;
        foreach ( $feature_keywords[ $selected ] as $keyword ) {
            if ( stripos( $feature_text, $keyword ) !== false ) {
                $matched = true;
                break;
            }
        }
        if ( ! $matched ) return false;
    }
    return true;
}

add_action('pre_get_posts', function($query) {
    if (!is_admin() && $query->is_main_query()) {

        // Homepage should show properties
        if ($query->is_front_page()) {
            $query->set('post_type', 'property'); // CPT slug
            $query->set('posts_per_page', 9);     // homepage grid
        }

        // Apply filters on archive + homepage + front page
        if ($query->is_post_type_archive('property') || $query->is_home() || $query->is_front_page()) {

            $meta_query = [];
            $tax_query  = [];

            // Locations (taxonomy)
            if (!empty($_GET['locations'])) {
                $tax_query[] = [
                    'taxonomy' => 'locations',
                    'field'    => 'slug',
                    'terms'    => sanitize_text_field($_GET['locations']),
                ];
            }

            // Property type (taxonomy)
            if (!empty($_GET['property-type'])) {
                $tax_query[] = [
                    'taxonomy' => 'property-type',
                    'field'    => 'slug',
                    'terms'    => sanitize_text_field($_GET['property-type']),
                ];
            }

            // Bedrooms (ACF field)
            if (!empty($_GET['bedrooms'])) {
                $meta_query[] = [
                    'key'     => 'bedrooms',
                    'value'   => (int) $_GET['bedrooms'],
                    'compare' => '='
                ];
            }

            // Reference Number (ACF field)
            if (!empty($_GET['reference_number'])) {
                $meta_query[] = [
                    'key'     => 'reference_number',
                    'value'   => sanitize_text_field($_GET['reference_number']),
                    'compare' => 'LIKE'
                ];
            }

            // Min Price (ACF field)
            if (!empty($_GET['price_min'])) {
                $meta_query[] = [
                    'key'     => 'for_sale_price',
                    'value'   => (int) $_GET['price_min'],
                    'compare' => '>=',
                    'type'    => 'NUMERIC'
                ];
            }

            // Max Price (ACF field)
            if (!empty($_GET['price_max'])) {
                $meta_query[] = [
                    'key'     => 'for_sale_price',
                    'value'   => (int) $_GET['price_max'],
                    'compare' => '<=',
                    'type'    => 'NUMERIC'
                ];
            }

            if (!empty($tax_query)) {
                $query->set('tax_query', $tax_query);
            }
            if (!empty($meta_query)) {
                $query->set('meta_query', $meta_query);
            }

            $query->set('posts_per_page', 12);

            // Sort by option
            $sortby = isset($_GET['sortby']) ? sanitize_text_field($_GET['sortby']) : 'recent';
            switch ($sortby) {
                case 'price_asc':
                    $query->set('orderby', 'meta_value_num');
                    $query->set('meta_key', 'for_sale_price');
                    $query->set('order', 'ASC');
                    break;
                case 'price_desc':
                    $query->set('orderby', 'meta_value_num');
                    $query->set('meta_key', 'for_sale_price');
                    $query->set('order', 'DESC');
                    break;
                default: // 'recent'
                    $query->set('orderby', 'date');
                    $query->set('order', 'DESC');
                    break;
            }

            // Feature filtering via post__in (repeater fields can't use meta_query)
            $selected_features = isset($_GET['features']) && is_array($_GET['features'])
                ? array_map('sanitize_text_field', array_slice($_GET['features'], 0, 20))
                : [];

            if ( ! empty( $selected_features ) ) {
                $all_properties = get_posts([
                    'post_type'      => 'property',
                    'post_status'    => 'publish',
                    'posts_per_page' => -1,
                    'fields'         => 'ids',
                    'suppress_filters' => true,
                ]);

                $filtered_ids = array_filter( $all_properties, function( $pid ) use ( $selected_features ) {
                    return property_matches_features_global( $pid, $selected_features );
                });

                $query->set( 'post__in', ! empty( $filtered_ids ) ? array_values( $filtered_ids ) : [0] );
            }
        }
    }
});

/**
 * Ensure external gallery is stored as comma-separated string, not array
 */
function ensure_external_gallery_is_string($post_id, $xml_node, $is_update) {
    if (get_post_type($post_id) !== 'property') {
        return;
    }
    
    $gallery = get_post_meta($post_id, 'external_property_gallery', true);
    
    // If it's an array, convert to comma-separated string
    if (is_array($gallery)) {
        $gallery_string = implode(', ', array_filter($gallery));
        update_post_meta($post_id, 'external_property_gallery', $gallery_string);
    }
}
