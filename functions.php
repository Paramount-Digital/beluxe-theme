/**
 * Get property featured image URL
 * Check for external gallery first, fallback to regular featured image
 * 
 * @param int $post_id The post ID
 * @return string|false The image URL or false if none found
 */
function get_property_featured_image_url($post_id = null) {
	if (!$post_id) {
		$post_id = get_the_ID();
	}
	
	// Check for external gallery (comma-separated URLs in textarea)
	$external_gallery = get_field('external_property_gallery', $post_id);
	if (!empty($external_gallery) && is_string($external_gallery)) {
		// Parse first URL from comma or newline separated list
		$urls = preg_split('/[,\r\n]+/', $external_gallery, -1, PREG_SPLIT_NO_EMPTY);
		$urls = array_map('trim', $urls);
		$urls = array_filter($urls);
		if (!empty($urls[0])) {
			return $urls[0];
		}
	}
	
	// Fallback to regular featured image
	$thumbnail_id = get_post_thumbnail_id($post_id);
	if ($thumbnail_id) {
		return wp_get_attachment_image_url($thumbnail_id, 'full');
	}
	
	return false;
}

/**
 * Get property featured image HTML
 * Check for external gallery first, fallback to regular featured image
 * 
 * @param int $post_id The post ID
 * @param string $size The image size
 * @param array $attr Additional attributes
 * @return string The image HTML
 */
function get_property_featured_image($post_id = null, $size = 'medium', $attr = array()) {
	if (!$post_id) {
		$post_id = get_the_ID();
	}
	
	// Check for external gallery (comma-separated URLs in textarea)
	$external_gallery = get_field('external_property_gallery', $post_id);
	if (!empty($external_gallery) && is_string($external_gallery)) {
		// Parse first URL from comma or newline separated list
		$urls = preg_split('/[,\r\n]+/', $external_gallery, -1, PREG_SPLIT_NO_EMPTY);
		$urls = array_map('trim', $urls);
		$urls = array_filter($urls);
		if (!empty($urls[0])) {
			$url = $urls[0];
			$alt = get_the_title($post_id);
			$class = isset($attr['class']) ? $attr['class'] : '';
			return '<img src="' . esc_url($url) . '" alt="' . esc_attr($alt) . '" class="' . esc_attr($class) . '" />';
		}
	}
	
	// Fallback to regular featured image
	return get_the_post_thumbnail($post_id, $size, $attr);
}

